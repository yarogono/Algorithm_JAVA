package baek;

import java.util.Scanner;

public class baek10869 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int k = sc.nextInt();

        System.out.println(n + k);
        System.out.println(n - k);
        System.out.println(n * k);
        System.out.println(n / k);
        System.out.println(n % k);
    }
}
